import pygame
import sys

CELL_SIZE = 20
WALL_COLOR = (0, 0, 0)
EMPTY_COLOR = (255, 255, 255)

class Worker:
    def __init__(self, username, x, y, color):
        self.username = username
        self.x = x
        self.y = y
        self.color = color

class WarehouseGrid:
    def __init__(self, grid, user_list):
        self.grid = grid
        self.grid_height = len(grid)
        self.grid_width = len(grid[0])
        self.screen = pygame.display.set_mode((self.grid_width * CELL_SIZE,
                                               self.grid_height * CELL_SIZE))
        self.workers = [Worker(u['username'], u['x'], u['y'], u['color']) for u in user_list]

    def draw_grid(self):
        for y, row in enumerate(self.grid):
            for x, cell in enumerate(row):
                color = WALL_COLOR if cell == 1 else EMPTY_COLOR
                pygame.draw.rect(self.screen, color, pygame.Rect(x * CELL_SIZE,
                                                                 y * CELL_SIZE,
                                                                 CELL_SIZE,
                                                                 CELL_SIZE))

    def draw_workers(self):
        for worker in self.workers:
            pygame.draw.rect(self.screen,
                             worker.color,
                             pygame.Rect(worker.x * CELL_SIZE,
                                         worker.y * CELL_SIZE,
                                         CELL_SIZE,
                                         CELL_SIZE))

    def move_worker(self, username, dx, dy):
        for worker in self.workers:
            if worker.username == username:
                new_x = worker.x + dx
                new_y = worker.y + dy
                if 0 <= new_x < self.grid_width and 0 <= new_y < self.grid_height:
                    if self.grid[new_y][new_x] == 0:
                        worker.x = new_x
                        worker.y = new_y

    def process_directions(self, directions):
        direction_map = {
            'left': (-1, 0),
            'right': (1, 0),
            'up': (0, -1),
            'down': (0, 1)
        }
        max_steps = max(len(cmds) for cmds in directions.values())
        for step in range(max_steps):
            for username, cmd_list in directions.items():
                if step < len(cmd_list):
                    direction = cmd_list[step]
                    if direction in direction_map:
                        dx, dy = direction_map[direction]
                        self.move_worker(username, dx, dy)
            self.update_display()
            pygame.time.wait(500)

    def update_display(self):
        self.screen.fill(EMPTY_COLOR)
        self.draw_grid()
        self.draw_workers()
        pygame.display.flip()

    def run(self, directions):
        self.process_directions(directions)
        pygame.quit()

